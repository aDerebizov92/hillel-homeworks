// === ДЗ 4.3 — Портрет користувача ===

// 1) Запитуємо дані. prompt повертає рядок або null (якщо натиснули "Скасувати").
const birthYear = prompt("У якому році ви народилися?");
const city      = prompt("У якому місті ви живете?");
const sport     = prompt("Який ваш улюблений вид спорту?");

// 2) Готуємо "столиці": якщо місто є ключем — беремо назву країни.
const capitals = {
  "Київ": "України",
  "Вашингтон": "США",
  "Лондон": "Великої Британії",
};

// 3) Готуємо види спорту та чемпіонів у них.
const champions = {
  "теніс": "Новак Джокович",
  "бокс": "Олександр Усик",
  "футбол": "Ліонель Мессі",
};

// 4) Збираємо підсумкове повідомлення по рядках у масив, потім з'єднаємо.
const lines = [];

// --- Вік ---
if (birthYear === null) {
  lines.push("Шкода, що Ви не захотіли ввести свій рік народження.");
} else {
  const age = 2026 - Number(birthYear);
  lines.push(`Ваш вік: ${age}`);
}

// --- Місто / столиця ---
if (city === null) {
  lines.push("Шкода, що Ви не захотіли ввести своє місто.");
} else if (capitals[city]) {
  // Місто знайшлося серед столиць — беремо країну з об'єкта capitals
  lines.push(`Ти живеш у столиці ${capitals[city]}!`);
} else {
  lines.push(`Ти живеш у місті ${city}.`);
}

// --- Спорт / чемпіон ---
if (sport === null) {
  lines.push("Шкода, що Ви не захотіли ввести свій вид спорту.");
} else if (champions[sport]) {
  lines.push(`Круто! Хочеш стати як ${champions[sport]}?`);
} else {
  lines.push(`Ваш улюблений спорт: ${sport}.`);
}

// 5) Виводимо ВСЕ в одному вікні. \n — перенос рядка всередині alert.
alert(lines.join("\n"));